public class AVD {
    private String name, device, manufacturer, target, tag, arch;

    public String getName() {
        return name;
    }

    public String getDevice() {
        return device;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public String getTarget() {
        return target;
    }

    public String getTag() {
        return tag;
    }

    public String getArch() {
        return arch;
    }
}
